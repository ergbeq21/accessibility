



<div class="space-y-6 h-screen  m-10 text-sm text-gray-700 " id="chapter2">
    <h2 class="text-6xl text-gray-800 text-center font-extrabold">What is <span class="text-indigo-600"> Digital Accessibility </span> and Why Does It Matter?</h2>
  
    <div class="flex justify-center items-center flex-col">
      <p class="text-center text-2xl">
        <strong>Digital accessibility</strong> (a11y) means designing and building websites and apps so that 
        <strong>people <br> with disabilities can use them in a meaningful <br>and equivalent way</strong>.
      </p>
    </div>
  

    <div class="flex justify-center items-center">

    <div class="grid grid-cols-1 md:grid-cols-2 gap-8">

      <div class="mt-2 shadow  p-5  font-mono hover:scale-102 transition-all duration-200">
        <h4 class="font-semibold">Individual Impact</h4>
        <ul class="list-disc pl-5 space-y-1">
          <li><strong>Over 1.3 billion people globally</strong> (15% of the population) identify as having a disability.</li>
          <li>Disabilities can be permanent, temporary, or situational (e.g., broken wrist, noisy environment).</li>
          <li>Types of impairments:
            <ul class="list-disc pl-5">
              <li><strong>Visual</strong> (blindness, low vision, color blindness)</li>
              <li><strong>Mobility</strong> (arthritis, paralysis)</li>
              <li><strong>Hearing</strong> (deafness, hearing loss)</li>
              <li><strong>Cognitive</strong> (ADHD, autism, dyslexia)</li>
              <li><strong>Seizure/vestibular</strong> (epilepsy, vertigo)</li>
              <li><strong>Speech</strong> (stuttering, dyspraxia)</li>
            </ul>
          </li>
        </ul>
      </div>
  

      <div class="mt-4   p-5 font-mono shadow hover:scale-102 transition-all duration-200">
        <h4 class="font-semibold">Business Impact</h4>
        <ul class="list-disc pl-5 space-y-1">
          <li>People with disabilities in the U.S. have <strong>$490 billion</strong> in disposable income.</li>
          <li>Disability market (including families/friends) influences <strong>53% of consumers</strong>.</li>
          <li>Accessibility fosters <strong>innovation</strong> and improves usability for all users.</li>
          <li>Examples: keyboards, voice control, automatic doors.</li>
        </ul>
      </div>
  

      <div class="mt-4 shadow font-mono p-5 hover:scale-102 transition-all duration-200">
        <h4 class="font-semibold">Legal Impact</h4>
        <ul class="list-disc pl-5 space-y-1">
          <li>Many countries (e.g., U.S., EU, Canada) have <strong>laws requiring digital accessibility</strong>.</li>
          <li>In the U.S., <strong>10+ lawsuits per day</strong> are filed for inaccessible digital products.</li>
          <li><strong>74% of lawsuits</strong> target ecommerce websites and apps.</li>
          <li>Lawsuits often hit the same business for both <strong>website and mobile app</strong>.</li>
        </ul>
      </div>  

    <div class="font-mono shadow p-5 hover:scale-102 transition-all duration-200 ">
      <h3 class="font-semibold text-lg text-gray-800">Key Tools for Accessibility</h3>
      <ul class="list-disc pl-5 space-y-1">
        <li>Screen readers</li>
        <li>Braille devices</li>
        <li>Captions and transcripts</li>
        <li>Keyboard navigation</li>
        <li>Voice input and AAC devices</li>
        <li>System settings to reduce animations</li>
      </ul>
    </div>

</div>
